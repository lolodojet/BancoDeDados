/*  MEDIA */
SELECT 
    ALUNO.NOME AS ALUNO,
    MATERIA.NOME AS MATERIA,

    CAST(
        ROUND(AVG(AVALIACOES.NOTA), 2)
    AS DECIMAL(5,2)) AS MEDIA

FROM AVALIACOES
JOIN MATRICULA ON AVALIACOES.ID_MATRICULA = MATRICULA.ID_MATRICULA
JOIN ALUNOS AS ALUNO ON MATRICULA.ID_ALUNO = ALUNO.MATRICULA
JOIN MATERIAS AS MATERIA ON MATRICULA.ID_MATERIA = MATERIA.SIGLA

GROUP BY ALUNO.NOME, MATERIA.NOME;

/* PORCENTAGEM DA FREQUENCIA */
SELECT 
    ALUNO.NOME AS ALUNO,
    MATERIA.NOME AS MATERIA,
    
    CAST(
        ROUND(
        SUM(CASE WHEN FREQUENCIA.PRESENCA = 1 THEN 1 ELSE 0 END) * 100.0 
        / NULLIF(COUNT(FREQUENCIA.ID_FREQUENCIA), 0)
    , 2) AS DECIMAL(5 , 2)) AS FREQUENCIAL_PERCENTUAL  

FROM FREQUENCIA
JOIN MATRICULA ON FREQUENCIA.ID_MATRICULA = MATRICULA.ID_MATRICULA
JOIN ALUNOS AS ALUNO ON MATRICULA.ID_ALUNO = ALUNO.MATRICULA
JOIN MATERIAS AS MATERIA ON MATRICULA.ID_MATERIA = MATERIA.SIGLA
GROUP BY ALUNO.NOME, MATERIA.NOME;


