CREATE DATABASE Universidade;

USE Universidade;
GO

CREATE TABLE ALUNOS
(
    MATRICULA INT IDENTITY(1,1) PRIMARY KEY,
    NOME VARCHAR(100) NOT NULL
);
GO

CREATE TABLE CURSOS
(
    CURSO CHAR(3) PRIMARY KEY,
    NOME VARCHAR(100) NOT NULL
);
GO

CREATE TABLE PROFESSOR
(
    PROFESSOR INT IDENTITY(1,1) PRIMARY KEY,
    NOME VARCHAR(100) NOT NULL
);
GO

CREATE TABLE MATERIAS
(
    SIGLA CHAR(3) PRIMARY KEY,
    NOME VARCHAR(100) NOT NULL,
    CARGAHORARIA INT NOT NULL,
    CURSO CHAR(3) NOT NULL,
    PROFESSOR INT NOT NULL,

    CONSTRAINT FK_MAT_CURSO
        FOREIGN KEY (CURSO) REFERENCES CURSOS(CURSO),

    CONSTRAINT FK_MAT_PROF
        FOREIGN KEY (PROFESSOR) REFERENCES PROFESSOR(PROFESSOR)
);
GO


CREATE TABLE MATRICULA
(
    ID_MATRICULA INT IDENTITY(1,1) PRIMARY KEY,

    ID_ALUNO INT NOT NULL,
    ID_MATERIA CHAR(3) NOT NULL,
    PERLETIVO INT NOT NULL,

    FOREIGN KEY (ID_ALUNO) REFERENCES ALUNOS(MATRICULA),
    FOREIGN KEY (ID_MATERIA) REFERENCES MATERIAS(SIGLA),

    UNIQUE (ID_ALUNO, ID_MATERIA, PERLETIVO)
);
GO


CREATE TABLE AVALIACOES
(
    ID_AVALIACAO INT IDENTITY(1,1) PRIMARY KEY,
    ID_MATRICULA INT NOT NULL,

    TIPO VARCHAR(20) NOT NULL, 
    NOTA DECIMAL(5,2) NOT NULL,

    CONSTRAINT FK_AVAL_MATR FOREIGN KEY (ID_MATRICULA) REFERENCES MATRICULA(ID_MATRICULA)
);
GO

CREATE TABLE FREQUENCIA
(
    ID_FREQUENCIA INT IDENTITY(1,1) PRIMARY KEY,
    ID_MATRICULA INT NOT NULL,

    DATA_AULA DATE NOT NULL,
    PRESENCA BIT NOT NULL,

    CONSTRAINT FK_FREQ_MATR FOREIGN KEY (ID_MATRICULA) REFERENCES MATRICULA(ID_MATRICULA)
);
GO

